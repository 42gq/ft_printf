#include "libft.h"

char	*ft_strcpy_until(char *dst, char *str, size_t i)
{
	size_t	k;

	k = 0;
	while (str[k] != '\0' && k < i)
	{
		dst[k] = str[k];
		k++;
	}
	dst[k] = '\0';
	return (dst);
}
